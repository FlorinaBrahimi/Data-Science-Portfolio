# Prompt the user to enter the base of the triangle and convert the input to an integer
base = int(input('Enter base: '))

# Prompt the user to enter the height of the triangle and convert the input to an integer
height = int(input('Enter height: '))

# Calculate the area of the triangle using the formula: area = 1/2 * base * height
area = 1/2 * base * height

# Print the calculated area of the triangle
# The output will display the area with the message 'Area is'
print('Area is', area)
