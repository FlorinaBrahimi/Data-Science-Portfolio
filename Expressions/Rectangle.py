# Prompt the user to enter the length of the rectangle and convert the input to an integer
length = int(input('Enter length of Rectangle: '))

# Prompt the user to enter the breadth of the rectangle and convert the input to an integer
breadth = int(input('Enter breadth of Rectangle: '))

# Calculate the area of the rectangle using the formula: area = length * breadth
area = length * breadth

# Print the calculated area of the rectangle
# The output will display the area with the message 'Area is'
print('Area is', area)
